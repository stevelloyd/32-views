\h3{Set your Pi up}

You'll need to get a Pi for these projects---there are lots of suppliers (see \em{notes}). You can buy the parts individually, or get a 'starter kit' which has all the modules to get going. Then you'll need to plug together the bits. There are several models of Pi, and the best if you're going to get the best out of these projects to start with, is the most recent Raspberry Pi 3 Model B+. In later projects, we'll use the Pi 3 Model B+'s little cub; the Zero W which is great as a little project machine, but for now, try and get the Big Bear of Pi.

The Raspberry Pi Foundation website has excellent resources on setup of a Pi---well it has excellent resources on so many topics that it's best just to take a look. But what we're looking for is the intial setup covered by \a[#:href "https://projects.raspberrypi.org/en/projects/raspberry-pi-setting-up"]{this}. 


